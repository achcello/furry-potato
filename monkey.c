task replay()
{
    writeDebugStream("\nReplaying\n");
    replaying = 1;
    int numRows = sizeof(auton) / sizeof(auton[0]);
    int numCols = sizeof(auton[0]) / sizeof(auton[0][0]);
    writeDebugStream("\nRows:%d, Cols:%d", numRows, numCols);
 
    for(int row=0; row < numRows; row++)
    {
        for(int i=0; i < numCols; i++)
            motor[i] = auton[row][i];
 
        wait1Msec(tick);
    }
 
    replaying = 0;
}
 
task record()
{
    continueRecording = 1;
    writeDebugStream("\nStarting\n");
    writeDebugStream("{");
    while(continueRecording)
    {
        writeDebugStream("{");
        for(int motorId=0; motorId < RT_MOTORS; motorId++)
        {
            writeDebugStream(motorId != RT_MOTORS - 1 ? "%d," : "%d", motor[motorId]);
        }
 
        writeDebugStream("},");
 
        writeDebugStream("\n");
        wait1Msec(tick);
    }
 
    writeDebugStream("}");
}